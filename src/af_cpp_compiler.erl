%% Find all compiled word definitions from the type registry
find_all_compiled_words() ->
    AllTypes = af_type:all_types(),
    lists:flatmap(fun(#af_type{ops = Ops}) ->
        lists:flatmap(fun({_OpName, OpList}) ->
            lists:filtermap(fun(#operation{name = N, sig_in = SI, sig_out = SO,
                                          source = {compiled, Body}}) ->
                {true, {N, SI, SO, Body}};
            (_) -> false
            end, OpList)
        end, maps:to_list(Ops))
    end, AllTypes).

%% Find all product types (types with auto-generated ops)
find_all_product_types() ->
    AllTypes = af_type:all_types(),
    lists:filtermap(fun(#af_type{name = Name, ops = Ops}) ->
        %% Product types have ops with source = auto
        HasAuto = lists:any(fun({_OpName, OpList}) ->
            lists:any(fun(#operation{source = auto}) -> true; (_) -> false end, OpList)
        end, maps:to_list(Ops)),
        case HasAuto andalso Name =/= 'Any' of
            true ->
                %% Extract fields from the constructor's sig_in
                ConstructorName = string:lowercase(atom_to_list(Name)),
                case find_constructor_fields(ConstructorName, Ops, AllTypes) of
                    {ok, Fields} -> {true, {Name, Fields}};
                    not_found -> false
                end;
            false -> false
        end
    end, AllTypes).

%% Find constructor fields by looking at the constructor op in Any
find_constructor_fields(ConstructorName, _Ops, AllTypes) ->
    %% Constructor is registered in Any
    AnyOps = case lists:keyfind('Any', #af_type.name, AllTypes) of
        #af_type{ops = O} -> O;
        false -> #{}
    end,
    case maps:get(ConstructorName, AnyOps, []) of
        [#operation{sig_in = SigIn, sig_out = [TypeName], source = auto} | _] ->
            %% SigIn is reversed field types; reconstruct field names from getters
            FieldTypes = lists:reverse(SigIn),
            FieldNames = find_field_names(TypeName, AllTypes),
            case length(FieldTypes) =:= length(FieldNames) of
                true -> {ok, lists:zip(FieldNames, FieldTypes)};
                false -> not_found
            end;
        _ -> not_found
    end.

%% Find field names by looking at getter ops in the type's dict
find_field_names(TypeName, AllTypes) ->
    case lists:keyfind(TypeName, #af_type.name, AllTypes) of
        #af_type{ops = Ops} ->
            GetterNames = [list_to_atom(N) || {N, [#operation{source = auto, sig_out = [_, TypeName2]} | _]} <- maps:to_list(Ops),
                           TypeName2 =:= TypeName],
            lists:sort(GetterNames);
        false -> []
    end.

%% Check if a name is a product field operation
is_product_field_op(Name) ->
    AllTypes = af_type:all_types(),
    %% Check setters (name ends with !)
    case lists:suffix("!", Name) of
        true ->
            FieldName = lists:droplast(Name),
            find_setter_type(FieldName, AllTypes);
        false ->
            find_getter_type(Name, AllTypes)
    end.

find_getter_type(Name, AllTypes) ->
    Result = lists:filtermap(fun(#af_type{name = TypeName, ops = Ops}) ->
        case TypeName of
            'Any' -> false;
            'Atom' -> false;
            _ ->
                case maps:get(Name, Ops, []) of
                    [#operation{source = auto} | _] ->
                        {true, {getter, atom_to_list(TypeName), Name}};
                    _ -> false
                end
        end
    end, AllTypes),
    case Result of
        [Hit | _] -> Hit;
        [] -> false
    end.

find_setter_type(FieldName, AllTypes) ->
    SetterName = FieldName ++ "!",
    %% Setters are registered in Any
    case lists:keyfind('Any', #af_type.name, AllTypes) of
        #af_type{ops = Ops} ->
            case maps:get(SetterName, Ops, []) of
                [#operation{source = auto, sig_out = [TypeName]} | _] ->
                    {setter, atom_to_list(TypeName), FieldName};
                _ -> false
            end;
        false -> false
    end.

%% Group word defs by name
group_by_name(WordDefs) ->
    Groups = lists:foldl(fun({Name, _, _, _} = Def, Acc) ->
        Existing = maps:get(Name, Acc, []),
        maps:put(Name, Existing ++ [Def], Acc)
    end, #{}, WordDefs),
    maps:to_list(Groups).

%% Convert A4 word name to valid C++ identifier
cpp_function_name(Name) ->
    Mapped = lists:map(fun(C) ->
        case C of
            $- -> $_;
            $? -> $Q;
            $! -> $X;
            $< -> $L;
            $> -> $G;
            $+ -> $P;
            $* -> $M;
            $/ -> $D;
            $= -> $E;
            _ -> C
        end
    end, Name),
    %% Prefix with af_ if it's a C++ reserved word
    case is_cpp_reserved(Mapped) of
        true -> "af_" ++ Mapped;
        false -> Mapped
    end.

is_cpp_reserved(Name) ->
    lists:member(Name, [
        "double", "float", "int", "long", "short", "char", "bool",
        "void", "auto", "class", "struct", "enum", "union", "namespace",
        "if", "else", "for", "while", "do", "switch", "case", "break",
        "continue", "return", "goto", "default", "try", "catch", "throw",
        "new", "delete", "this", "true", "false", "const", "static",
        "extern", "inline", "virtual", "override", "final", "public",
        "private", "protected", "template", "typename", "using",
        "operator", "sizeof", "alignof", "decltype", "constexpr",
        "nullptr", "noexcept", "register", "volatile", "mutable"
    ]).

%% C++ parameter type from A4 type
cpp_param_type('Int') -> "int64_t";
cpp_param_type('Float') -> "double";
cpp_param_type('Bool') -> "bool";
cpp_param_type('String') -> "const std::string&";
cpp_param_type(_) -> "const Value&".

%% Make a Value from a C++ variable
cpp_make_value('Int', Var) -> ["Value::make_int(", Var, ")"];
cpp_make_value('Float', Var) -> ["Value::make_float(", Var, ")"];
cpp_make_value('Bool', Var) -> ["Value::make_bool(", Var, ")"];
cpp_make_value('String', Var) -> ["Value::make_string(", Var, ")"];
cpp_make_value(_, Var) -> Var.

%% Generate literal value for match conditions
cpp_literal_value('Int', V) when is_integer(V) ->
    ["Value::make_int(", integer_to_list(V), ")"];
cpp_literal_value('Bool', true) -> "Value::make_bool(true)";
cpp_literal_value('Bool', false) -> "Value::make_bool(false)";
cpp_literal_value('String', V) when is_list(V) ->
    ["Value::make_string(\"", escape_cpp_string(V), "\")"];
cpp_literal_value(Type, V) ->
    ["Value::make_atom(\"", io_lib:format("~p:~p", [Type, V]), "\")"].

%% Try to parse a token as a literal
try_literal(Name) ->
    case catch list_to_integer(Name) of
        V when is_integer(V) -> {int, V};
        _ ->
            case Name of
                "true" -> {bool, true};
                "false" -> {bool, false};
                _ -> not_found
            end
    end.

%% Format signature for comments
format_sig([]) -> "()";
format_sig(Sig) ->
    Items = [format_sig_item(S) || S <- lists:reverse(Sig)],
    lists:join(" ", Items).

format_sig_item({Type, Value}) ->
    io_lib:format("~p(~p)", [Type, Value]);
format_sig_item(Type) when is_atom(Type) ->
    atom_to_list(Type).

%% Escape string for C++ string literal
escape_cpp_string(S) ->
    lists:flatmap(fun(C) ->
        case C of
            $\\ -> "\\\\";
            $" -> "\\\"";
            $\n -> "\\n";
            $\t -> "\\t";
            _ -> [C]
        end
    end, S).
