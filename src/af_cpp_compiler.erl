generate_cpp(ModName, #{words := WordDefs, types := ProductTypes}) ->
    Header = generate_header(ModName),
    TypeDefs = generate_type_defs(ProductTypes),
    {FwdDecls, FunDefs} = generate_functions(WordDefs),
    Main = generate_main(ModName),
    iolist_to_binary([
        Header, "\n",
        TypeDefs, "\n",
        FwdDecls, "\n",
        FunDefs, "\n",
        Main
    ]).

