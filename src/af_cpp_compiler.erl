%% Product type field access — handled via runtime dispatch on type_name
%% This is a fallback; known product ops get specialized code in generate_body
translate_to_cpp(Name, WordName) ->
    %% Check if this is a self-recursive call
    case Name =:= WordName of
        true ->
            ["    ", cpp_function_name(Name), "(s);\n"];
        false ->
            %% Check if it's a known word or a product type operation
            case is_product_field_op(Name) of
                {getter, TypeName, FieldName} ->
                    ["    op_", string:lowercase(TypeName), "_", FieldName, "(s);\n"];
                {setter, TypeName, FieldName} ->
                    ["    op_", string:lowercase(TypeName), "_", FieldName, "_set(s);\n"];
                {constructor, ConstructorName} ->
                    ["    make_", ConstructorName, "_push(s);\n"];
                false ->
                    %% Try as a compiled word call
                    case af_word_compiler:find_compiled_word_defs(Name) of
                        [_ | _] ->
                            ["    ", cpp_function_name(Name), "(s);\n"];
                        [] ->
                            %% Literal or unknown — try int/bool literal
                            case try_literal(Name) of
                                {int, V} ->
                                    ["    push(s, Value::make_int(", integer_to_list(V), "));\n"];
                                {bool, true} ->
                                    "    push(s, Value::make_bool(true));\n";
                                {bool, false} ->
                                    "    push(s, Value::make_bool(false));\n";
                                {string, S} ->
                                    ["    push(s, Value::make_string(\"", escape_cpp_string(S), "\"));\n"];
                                not_found ->
                                    ["    // Unknown op: ", Name, "\n",
                                     "    push(s, Value::make_atom(\"", escape_cpp_string(Name), "\"));\n"]
                            end
                    end
            end
    end.

