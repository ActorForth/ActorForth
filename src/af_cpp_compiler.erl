%% Initialize the compile-to-cpp word in the Any dictionary.
init() ->
    af_type:add_op('Any', #operation{
        name = "compile-to-cpp",
        sig_in = ['String', 'String'],
        sig_out = [],
        impl = fun op_compile_to_cpp/1
    }),
    ok.

%% ActorForth word: compile-to-cpp
%% ( output_path module_name -- )
%% Interprets current word definitions and generates C++ source file.
op_compile_to_cpp(Cont) ->
    [{'String', ModNameBin}, {'String', PathBin} | Rest] = Cont#continuation.data_stack,
    ModName = binary_to_list(ModNameBin),
    Path = binary_to_list(PathBin),
    WordDefs = find_all_compiled_words(),
    ProductTypes = find_all_product_types(),
    CppCode = generate_cpp(ModName, #{words => WordDefs, types => ProductTypes}),
    ok = file:write_file(Path, CppCode),
    Cont#continuation{data_stack = Rest}.

%% Compile an .a4 file to C++ source.
compile_file_to_cpp(A4Path, CppPath) ->
    {ok, Content} = file:read_file(A4Path),
    af_repl:init_types(),
    init(),
    C = af_interpreter:new_continuation(),
    Tokens = af_parser:parse(binary_to_list(Content), A4Path),
    af_interpreter:interpret_tokens(Tokens, C),
    WordDefs = find_all_compiled_words(),
    ProductTypes = find_all_product_types(),
    ModName = filename:rootname(filename:basename(A4Path)),
    CppCode = generate_cpp(ModName, #{words => WordDefs, types => ProductTypes}),
    ok = file:write_file(CppPath, CppCode),
    {ok, CppPath}.

%% Compile word definitions to C++ from the interpreter state.
compile_to_cpp(ModName, CppPath) ->
    WordDefs = find_all_compiled_words(),
    ProductTypes = find_all_product_types(),
    CppCode = generate_cpp(ModName, #{words => WordDefs, types => ProductTypes}),
    ok = file:write_file(CppPath, CppCode),
    {ok, CppPath}.

