%%% ===================================================================
%%% Product Type Generation
%%% ===================================================================

generate_type_defs([]) -> [];
generate_type_defs(ProductTypes) ->
    [
        "// Product type constructors and accessors\n\n",
        [generate_product_type(T) || T <- ProductTypes]
    ].

generate_product_type({TypeName, Fields}) ->
    NameStr = atom_to_list(TypeName),
    ConstructorName = string:lowercase(NameStr),
    [
        "// Product type: ", NameStr, "\n",
        "inline Value make_", ConstructorName, "(", generate_constructor_params(Fields), ") {\n",
        "    ProductFields fields = {\n",
        generate_field_inits(Fields),
        "    };\n",
        "    return Value::make_product(\"", NameStr, "\", fields);\n",
        "}\n\n",
        %% Stack-based constructor (pops N values, pushes product)
        generate_stack_constructor(NameStr, ConstructorName, Fields),
        %% Getter functions
        [generate_getter(NameStr, FName, FType) || {FName, FType} <- Fields],
        %% Setter functions
        [generate_setter(NameStr, FName, FType) || {FName, FType} <- Fields],
        "\n"
    ].

generate_stack_constructor(TypeName, ConstructorName, Fields) ->
    %% Pop fields in reverse order (TOS = last field), construct product, push
    RevFields = lists:reverse(Fields),
    PopVars = [["    auto ", atom_to_list(FName), "_val = pop(s);\n"]
               || {FName, _FType} <- RevFields],
    FieldPairs = [["        {\"", atom_to_list(FName), "\", ", atom_to_list(FName), "_val}"]
                  || {FName, _FType} <- Fields],
    [
        "// Stack-based constructor: pops ", integer_to_list(length(Fields)),
        " values, pushes ", TypeName, "\n",
        "inline void make_", ConstructorName, "_push(Stack& s) {\n",
        PopVars,
        "    ProductFields fields = {\n",
        lists:join(",\n", FieldPairs), "\n",
        "    };\n",
        "    push(s, Value::make_product(\"", TypeName, "\", fields));\n",
        "}\n\n"
    ].

generate_constructor_params(Fields) ->
    Params = [generate_param(FName, FType) || {FName, FType} <- Fields],
    lists:join(", ", Params).

generate_param(FName, FType) ->
    [cpp_param_type(FType), " ", atom_to_list(FName)].

generate_field_inits(Fields) ->
    Inits = [["        {\"", atom_to_list(FName), "\", ",
              cpp_make_value(FType, atom_to_list(FName)), "}"]
             || {FName, FType} <- Fields],
    lists:join(",\n", Inits) ++ "\n".

generate_getter(TypeName, FName, _FType) ->
    FNameStr = atom_to_list(FName),
    [
        "// ", TypeName, ".", FNameStr, " (non-destructive getter)\n",
        "inline void op_", string:lowercase(TypeName), "_", FNameStr, "(Stack& s) {\n",
        "    auto& instance = s.back();\n",
        "    push(s, instance.field(\"", FNameStr, "\"));\n",
        "}\n\n"
    ].

generate_setter(TypeName, FName, _FType) ->
    FNameStr = atom_to_list(FName),
    [
        "// ", TypeName, ".", FNameStr, "! (setter)\n",
        "inline void op_", string:lowercase(TypeName), "_", FNameStr, "_set(Stack& s) {\n",
        "    auto new_val = pop(s);\n",
        "    auto instance = pop(s);\n",
        "    push(s, instance.with_field(\"", FNameStr, "\", new_val));\n",
        "}\n\n"
    ].

%%% ===================================================================
%%% Function Generation
%%% ===================================================================

generate_functions(WordDefs) ->
    Grouped = group_by_name(WordDefs),
    FwdDecls = ["// Forward declarations\n",
                [generate_fwd_decl(Name) || {Name, _} <- Grouped],
                "\n"],
    FunDefs = [generate_word_group(Name, Defs) || {Name, Defs} <- Grouped],
    {FwdDecls, FunDefs}.

generate_fwd_decl(Name) ->
    CppName = cpp_function_name(Name),
    ["void ", CppName, "(Stack& s);\n"].

generate_word_group(Name, Defs) ->
    CppName = cpp_function_name(Name),
    case Defs of
        [SingleDef] ->
            generate_single_function(CppName, Name, SingleDef);
        MultipleDefs ->
            generate_multi_clause_function(CppName, Name, MultipleDefs)
    end.

generate_single_function(CppName, Name, {_Name, SigIn, _SigOut, Body}) ->
    BodyCode = generate_body(Body, SigIn, Name),
    [
        "// Word: ", Name, "\n",
        "// Signature: ", format_sig(SigIn), " -> ", format_sig(_SigOut), "\n",
        "void ", CppName, "(Stack& s) {\n",
        BodyCode,
        "}\n\n"
    ].

generate_multi_clause_function(CppName, Name, Defs) ->
    %% Value-constrained clauses first, general clause last
    {Constrained, General} = lists:partition(fun({_, SigIn, _, _}) ->
        lists:any(fun(S) -> is_tuple(S) end, SigIn)
    end, Defs),
    Clauses = Constrained ++ General,
    [
        "// Word: ", Name, " (multi-clause)\n",
        "void ", CppName, "(Stack& s) {\n",
        generate_clauses(Clauses, Name, true),
        "}\n\n"
    ].

generate_clauses([], _Name, _First) -> [];
generate_clauses([{_, SigIn, _SigOut, Body} | Rest], Name, First) ->
    HasConstraint = lists:any(fun(S) -> is_tuple(S) end, SigIn),
    case HasConstraint of
        true ->
            Cond = generate_match_condition(SigIn),
            Keyword = case First of true -> "if"; false -> "} else if" end,
            [
                "    ", Keyword, " (", Cond, ") {\n",
                generate_body(Body, SigIn, Name),
                generate_clauses(Rest, Name, false)
            ];
        false ->
            case First of
                true ->
                    [
                        generate_body(Body, SigIn, Name),
                        generate_clauses(Rest, Name, false)
                    ];
                false ->
                    [
                        "    } else {\n",
                        generate_body(Body, SigIn, Name),
                        "    }\n",
                        generate_clauses(Rest, Name, false)
                    ]
            end
    end.

generate_match_condition(SigIn) ->
    %% Generate condition checking value constraints
    %% SigIn is TOS-first, so index 0 = back of vector
    Conditions = lists:filtermap(fun({Idx, SigEntry}) ->
        case SigEntry of
            {Type, Value} ->
                StackIdx = io_lib:format("s[s.size() - ~B]", [Idx]),
                {true, [StackIdx, " == ", cpp_literal_value(Type, Value)]};
            _ -> false
        end
    end, lists:zip(lists:seq(1, length(SigIn)), SigIn)),
    lists:join(" && ", Conditions).

%%% ===================================================================
%%% Body Code Generation
%%% ===================================================================

generate_body(Body, _SigIn, WordName) ->
    Ops = [generate_op(Op, WordName) || Op <- Body],
    Ops.

generate_op(#operation{name = Name}, WordName) ->
    translate_to_cpp(Name, WordName).

